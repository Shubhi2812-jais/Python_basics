#  arithmetic operators

x=10
y=5
print(x+y)
print(x-y) 
print(x*y)
print(x/y)
print(x%y)
print(x**y)

# comparison operators
print(x==y) #false
print(x>=y) #true
print(x<=y) #false
print(x>y) #true
print((x<y)) #false

# logical operators

print(x>y and x<y) #false
print(x>y or x<y) #true
print(not(x>y)) #false
print(not(x<y)) #true

